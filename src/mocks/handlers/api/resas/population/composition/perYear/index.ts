const handlers = [
  http.get('*/api/resas/population/composition/perYear', async ({ request }) => {
    const url = new URL(request.url);
    const prefCode = url.searchParams.get('prefCode');
    const cityCode = url.searchParams.get('cityCode');

    if (!prefCode || !cityCode) {
      return new HttpResponse(null, {
        status: 400,
      });
    }

    // MSWのモックサーバーでtimeoutパラメーターを指定することで、リクエストの遅延をシミュレートできます
    const timeout = url.searchParams.get('timeout');
    if (timeout) {
      await delay(Number(timeout));
    }

    return HttpResponse.json({
      message: null,
      result: mockPopulationCompositionPerYear[Number(prefCode) as PrefCode],
    });
  }),
];

export default handlers;
